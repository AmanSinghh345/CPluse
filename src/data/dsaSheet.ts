function slugify(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function questionUrl(title: string, source?: string) {
  if (!source) return undefined;
  if (source.includes("LeetCode")) return `https://leetcode.com/problems/${slugify(title)}/`;
  if (source.includes("GFG")) return `https://www.geeksforgeeks.org/search/?q=${encodeURIComponent(title)}`;
  if (source.includes("Striver")) return `https://www.google.com/search?q=${encodeURIComponent(`Striver ${title}`)}`;
  if (source.includes("InterviewBit")) return `https://www.interviewbit.com/search/?q=${encodeURIComponent(title)}`;
  return undefined;
}

